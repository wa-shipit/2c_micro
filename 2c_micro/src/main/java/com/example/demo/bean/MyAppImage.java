package com.example.demo.bean;

public class MyAppImage {
	private String name;
	private String imageBase64;
	private String msg;
	private String prapra;
	private String dekita;

	public String getDekita() {
		return dekita;
	}

	public void setDekita(String dekita) {
		this.dekita = dekita;
	}

	public String getPrapra() {
		return prapra;
	}

	public void setPrapra(String prapra) {
		this.prapra = prapra;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImageBase64() {
		return imageBase64;
	}

	public void setImageBase64(String imageBase64) {
		this.imageBase64 = imageBase64;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}
}



